            <h2 class="text-left text-red">5. Les droits des personnes concernées</h2>
            <ul class="text-left text-dark">
                <li>5.1 Les droits d’accès et d’information</li>
                <p>Comme l’indique la loi Informatique et Libertés de 1978, les données personnelles que nous traitons sont
                    à votre disposition et vous avez donc un droit d’accès et d’information à leur égard.
                    <br> Si vous nous le demandez, nous vous ferons part de la raison pour laquelle nous traitons vos données
                    personnelles, du type d’informations dont nous disposons vous concernant, du délai imposé avant la suppression
                    de ces données et des personnes susceptibles de recevoir ces informations personnelles. Cette politique
                    des données fait déjà état d’un bon nombre d’informations en ce qui concerne l’utilisation de vos données
                    personnelles.
                </p>
                <li>5.2  Le droit de rectification</li>
                <p>Comme l’indique la loi Informatique et Libertés de 1978, les informations que nous traitons sont susceptibles
                    d’être rectifiées par les utilisateurs. Vous êtes en droit de les modifier.
                    <br> Dès lors, si nous traitons des données vous concernant qui soient erronées, il est important que vous
                    nous contactiez afin que nous puissions effectuer leur modification.
                </p>
                <li>5.3 Droit d’opposition</li>
                <p>Vous êtes en droit, comme l’indique le Règlement Général sur la Protection des Données, de vous opposer au
                    traitement de vos données personnelles.
                    <br> Afin d’être reçue, votre demande doit respecter les conditions d’exercice de ce droit (cf. l’article
                    21 du règlement).
                </p>
                <li>5.4 Le droit à l’effacement</li>
                <p>Vous pouvez demander la suppression de vos données personnelles. Ce droit à l’oubli est indiqué dans le Règlement
                    Général sur la Protection des Données.
                    <br> Afin d’être reçue, votre demande doit respecter les conditions d’exercice de ce droit (cf. l’article
                    17 du règlement).
                </p>
                <li>5.5 Le droit à la limitation du traitement</li>
                <p>Vous pouvez demander la limitation du traitement de vos données personnelles. Ce droit est indiqué dans le
                    Règlement Général sur la Protection des Données.
                    <br> Afin d’être reçue, votre demande doit respecter les conditions d’exercice de ce droit (cf. l’article
                    18 du règlement).
                </p>
                <li>5.6 Le droit à la portabilité</li>
                <p>Vous pouvez demander la portabilité de vos données personnelles. Ce droit est indiqué dans le Règlement Général
                    sur la Protection des Données.
                    <br> Afin d’être reçue, votre demande doit respecter les conditions d’exercice de ce droit (cf. l’article
                    20 du règlement).
                </p>
                <li>5.7 Le droit au refus de décisions individuelles automatisées, y compris le profilage</li>
                <p>Vous pouvez refuser les décisions individuelles automatisées. Ce droit est indiqué dans le règlement général
                    sur la protection des données.
                    <br> Afin d’être reçue, votre demande doit respecter les conditions d’exercice de ce droit (cf. l’article
                    22 du règlement).
                </p>
            </ul>

            <h2 class="text-left text-red"> 6. Les modalités d’exercice des droits des personnes concernées</h2>
            <p class="text-left text-dark">Si le traitement des données personnelles vous concerne et que vous souhaitez exercer l’un de    vos droits mentionnés
                dans le point 5, merci de nous adresser un courrier par voie électronique à "ADMIN_TFC" ou bien par voie postale à notre siège social du TFC.
                <br> Le traitement de ces demandes peut être refusé par le TFC (article 12.5 RGPD) si elles se révèlent excessives ou infondées, par exemple lorsqu’elles sont répétitives.
            </p>

            <h2 class="text-left text-red">7. Réclamation auprès de l’autorité de contrôle</h2>
            <p class="text-left text-dark">Dans le cas où le TFC ne respecte pas ses obligations, la personne concernée par un traitement de données personnelles
                est en droit d’introduire une réclamation auprès de la CNIL (coordonnées : https://www.cnil.fr/fr/vous-souhaitez-contacter-la-cnil),
                autorité de contrôle compétente en matière de protection des données personnelles.<br><br>
            </p>

            <h2 class="text-left text-red">8. La notification d’une violation de données personnelles</h2>
            <p class="text-left text-dark">Le TFC s’engage, afin d’éviter toute violation de données personnelles, à prendre toutes les      mesures adéquates et pertinentes. 
                <br>Si une violation de données personnelles venait toutefois à survenir, nous nous engageons à notifier la CNIL de cette violation. Conformément à l’article 33 du RGPD, cette notification se ferait dans les meilleurs délais et dans la mesure du possible, 72 heures après en avoir pris connaissance au plus tard, sauf dans le cas où la violation en question ne soit pas susceptible d’entraîner un risque pour les droits et libertés des personnes concernées.
                <br>Comme l’indique l’article 34 du RGPD, ce devoir de notification à l’autorité de contrôle pourra être accompagné d’une communication à la personne concernée.<br><br>
            </p>

