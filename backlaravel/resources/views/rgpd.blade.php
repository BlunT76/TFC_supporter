                <br>
                <br> Définitions utiles :
                <br> « Donnée à caractère personnel » : Toute information identifiant directement ou indirectement une personne
                physique (ex : nom, n° de téléphone, commune de résidence, adresse mail…) ;
                <br> « Traitement » : Toute opération, ou ensemble d’opérations, portant sur de telles données, quel que soit
                le procédé utilisé (collecte, enregistrement, organisation, conservation, adaptation, modification, extraction,
                consultation, utilisation, communication par transmission diffusion ou toute autre forme de mise à disposition,
                rapprochement ou interconnexion, verrouillage, effacement ou destruction, …) ;
                <br> « Fichier » : Tout ensemble structuré de données à caractère personnel accessibles selon des critères déterminés,
                que cet ensemble soit centralisé, décentralisé ou réparti de manière fonctionnelle ou géographique ;
                <br> « Responsable du/de traitement » : Le responsable d’un traitement de données à caractère personnel est,
                sauf désignation expresse par les dispositions législatives ou réglementaires relatives à ce traitement,
                la personne, l’autorité publique, le service ou l’organisme qui détermine ses finalités et ses moyens. En
                pratique et en général, il s’agit de la personne morale incarnée par son représentant légal ;
                <br> « Sous-traitant » : La personne physique ou morale, l’autorité publique, le service ou un autre organisme
                qui traite des données à caractère personnel pour le compte du responsable du traitement ;
                <br> « Violation de données à caractère personnel » : Une violation de la sécurité entraînant, de manière accidentelle
                ou illicite, la destruction, la perte, l’altération, la divulgation non autorisée de données à caractère
                personnel transmises, conservées ou traitées d’une autre manière, ou l’accès non autorisé à de telles données ;
                <br> « Consentement » de la personne concernée : toute manifestation de volonté libre, spécifique, éclairée et
                univoque par laquelle la personne concernée accepte, par une déclaration ou par un acte positif clair, que
                des données à caractère personnel la concernant fasse l’objet d’un traitement ;
                <br> « Tiers » : Une personne physique ou morale, une autorité publique, un service ou un organisme autre que
                la personne concernée, le responsable du traitement, le sous-traitant et les personnes qui, placées sous
                l’autorité directe du responsable de traitement ou du sous-traitant, sont autorisées à traiter les données
                à caractère personnel.
